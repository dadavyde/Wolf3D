char	*ft_strcpy(char *dst, const char *src)
{
	int		idx;

	idx = 0;
	while (src[idx] != '\0')
	{
		dst[idx] = src[idx];
		idx++;
	}
	dst[idx] = '\0';
	return (dst);
}
