char	*ft_strcat(char *s1, const char *s2)
{
	int		idx;
	int		idx2;

	idx = 0;
	while (s1[idx] != '\0')
		idx++;
	idx2 = 0;
	while (s2[idx2] != '\0')
	{
		s1[idx] = s2[idx2];
		idx++;
		idx2++;
	}
	s1[idx] = '\0';
	return (s1);
}
